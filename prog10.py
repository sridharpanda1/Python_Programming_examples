# Write a program to take a single digit nuber from the keyboard and print is value in English word
n = int(input('enter a number: '))
if n == 0:
    print('ZERO')
elif n == 1:
    print('ONE')
elif n == 2:
    print('TEO')
elif n == 3:
    print('THREE')
elif n == 4:
    print('FOUR')
elif n == 5:
    print('FIVE')
elif n == 6:
    print('FIX')
elif n == 7:
    print('SEVEN')
elif n == 8:
    print('EIGHT')
elif n == 9:
    print('NINE')
else:
    print('Enter a valid digit from keyboard')